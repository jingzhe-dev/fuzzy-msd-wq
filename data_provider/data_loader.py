class Dataset_DO_hour(Dataset):
    """公开水质数据集的滑动窗口 Dataset。

    输入 CSV 需要包含 `date` 列、多个水质指标列和目标列。
    当 `features='MS'` 时，目标列会被移动到最后一列。
    """

    def __init__(self, root_path, flag='train', size=None,
                 features='MS', data_path='ailianwan.csv',
                 target='rjy', scale=True, timeenc=0, freq='h', seasonal_patterns=None):

        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))

        df_raw = normalize_water_quality_frame(df_raw, self.target)
 
        # 按时间顺序切分：前 60% 训练，中间 20% 验证，后 20% 测试。
        # 验证/测试起点向前回退 seq_len，保证第一个窗口有完整历史输入。
        num_train = int(len(df_raw) * 0.6)
        num_test = int(len(df_raw) * 0.2)
        num_vali = len(df_raw) - num_train - num_test
        
        border1s = [0, num_train - self.seq_len, len(df_raw) - num_test - self.seq_len]
